# ---------------------------------------------------------------- name conventions
def fn(n):
    """repo-wide normalized-name convention (matches statmenu/fp_personnel/player_funnels keys)."""
    n = str(n).strip().lower()
    n = re.sub(r"\s+(jr|sr|ii|iii|iv|v)\.?$", "", n)
    return n.replace(".", "").replace("'", "").replace("-", " ")

def rslug(n):
    """url-safe route slug (for #/player/<slug>). bc.slug stays the obsidian filename."""
    s = re.sub(r"[^a-z0-9 ]", "", fn(n))
    return re.sub(r"\s+", "-", s.strip()) or "unknown"

# ---------------------------------------------------------------- loaders (graceful)
def jload(*parts):
    p = os.path.join(*parts)
    try:
        return json.load(open(p, encoding="utf-8")) if os.path.exists(p) else None
    except Exception as e:
        bc.log(f"warroom: could not parse {p}: {e}")
        return None

# ---------------------------------------------------------------- v1 status helpers (reused)
def run_status(vault):
    p = os.path.join(vault, "_status", "last-run.md")
    if not os.path.exists(p): return ("red", "no run card — has the ingest ever run?")
    txt = open(p, encoding="utf-8").read()
    call = next((l for l in txt.split("\n") if "[!" in l), "")
    msg = call.split("]", 1)[-1].strip() if call else "unknown"
    lamp = "red" if "❌" in call or "danger" in call else ("amber" if "⚠" in call else "green")
    stats = next((l.lstrip("- ").strip() for l in txt.split("\n") if l.strip().startswith("- **")), "")
    return (lamp, (msg + (" · " + stats if stats else "")).replace("**", ""))

def weekly_status(vault):
    p = os.path.join(vault, "_status", "weekly.log")
    if not os.path.exists(p): return ("amber", "weekly model refresh: never run")
    txt = open(p, encoding="utf-8", errors="replace").read()
    runs = re.findall(r"===== WEEKLY MODEL REFRESH (.+?) ·", txt)
    if not runs: return ("amber", "weekly model refresh: never run")
    return ("green", f"weekly quant refresh — last: {runs[-1].strip()}")

def lines_status(repo):
    p = os.path.join(repo, "ffdataroma_draft_guide_export/ffdataroma/csv/weekly-vegas-lines.csv")
    if not os.path.exists(p): return ("amber", "vegas lines: source missing")
    d = (datetime.now() - datetime.fromtimestamp(os.path.getmtime(p))).days
    return ("green" if d <= 14 else "amber", f"vegas lines: {d}d old")

def fm_field(head, key):
    m = re.search(rf"^{key}:\s*(.+)$", head, re.M)
    return m.group(1).strip() if m else ""

def todays_tweets(vault):
    today = date.today().strftime("%Y-%m-%d"); month = date.today().strftime("%Y-%m")
    out = []
    for p in glob.glob(os.path.join(vault, "Tweets", month, f"{today}*.md")):
        txt = open(p, encoding="utf-8", errors="replace").read()
        parts = txt.split("---", 2)
        if len(parts) < 3: continue
        head = parts[1]
        ments = re.findall(r"\[\[([^\]]+)\]\]", head.split("mentions:", 1)[1].split("\n", 1)[0]) if "mentions:" in head else []
        out.append({"url": fm_field(head, "url"), "mentions": ments})
    return out

