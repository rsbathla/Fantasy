def _game_env(gs):
    """per-team underdog count + avg implied across all simmed weeks (v1-style calc,
    same as brain_concepts._environment)."""
    env = {}
    for wk in (gs.get("weeks") or {}).values():
        for g in wk.get("games", []):
            v = g.get("vegas") or {}
            for ab in g.get("teams", []):
                e = env.setdefault(ab, {"dogs": 0, "gms": 0, "imps": []})
                imp = (v.get("imp") or {}).get(ab)
                if imp: e["imps"].append(imp)
                fav = v.get("spread_fav"); line = -v.get("spread", 0) if fav == ab else v.get("spread", 0)
                if line > 0: e["dogs"] += 1
                e["gms"] += 1
    return {ab: {"dogs": e["dogs"], "gms": e["gms"],
                 "imp": round(sum(e["imps"]) / len(e["imps"]), 1) if e["imps"] else None}
            for ab, e in env.items()}

def _trim(s, n):
    s = str(s or "")
    return s if len(s) <= n else s[:n - 1].rstrip() + "…"

# coverage vocabulary — what counts as a "coverage scheme" report from the brain.
# CONTEXTUAL on purpose: bare man/zone/press/nickel/dime fire on idioms ("head man",
# "stopped on a dime", "press conference") and run-game text ("zone-blocking").
COV_RE = re.compile(r"""
    \bman[- ]to[- ]man\b | \bman\s+coverage\b | \bpress\s+man\b | \bman[- ]heavy\b
  | \bman\s+rate\b | \b(?:in|vs\.?|against|play(?:s|ing)?)\s+man\b
  | \bzone\s+coverage\b | \bzone\s+rate\b | \bmatch\s+zone\b | \bzone[- ]heavy\b
  | \b(?:in|vs\.?|against|play(?:s|ing)?)\s+zone\b
  | \btwo[- ]high\b | \bsingle[- ]high\b | \bcover\s?[0-6]\b
  | \bquarters\s+coverage\b | \bpalms\b | \bmatch\s+coverage\b
  | \bpress\s+coverage\b | \bpress\s+rate\b
  | \bblitz | \bsim(?:ulated)?\s+pressure\b | \bshell\b
  | \bnickel\s+(?:package|defense|personnel|corner|back)\b
  | \bdime\s+(?:package|defense|personnel|back)\b
""", re.I | re.X)

def build_dc_intel(cs26, bi):
    """per new-DC team: coverage-vocab-matched intel from brain_intel coaches[dc].tw/.src26
    + teams[team].tw — deduped, newest first, top 3 kept (full match count reported)."""
    coaches = bi.get("coaches") or {}
    by_abbr = {e.get("abbr"): e for e in (bi.get("teams") or {}).values()
               if isinstance(e, dict) and e.get("abbr")}
    out = {}
    for ab, e in cs26.items():
        if not (isinstance(e, dict) and e.get("dc_new")): continue
        pool = []
        ce = coaches.get(e.get("dc_name") or "") or {}
        for t in (ce.get("tw") or []):
            pool.append({"d": t.get("d", ""), "a": t.get("a", ""), "t": t.get("t", ""), "u": t.get("u", "")})
        for s in (ce.get("src26") or []):
            pool.append({"d": s.get("d", ""), "a": s.get("o", ""), "t": s.get("t", ""), "u": ""})
        for t in ((by_abbr.get(ab) or {}).get("tw") or []):
            pool.append({"d": t.get("d", ""), "a": t.get("a", ""), "t": t.get("t", ""), "u": t.get("u", "")})
        hits, seen = [], set()
        for it in pool:
            txt = str(it.get("t") or "")
            if not COV_RE.search(txt): continue
            dup = it.get("u") or txt[:80]
            if dup in seen: continue
            seen.add(dup)
            hits.append(it)
        hits.sort(key=lambda x: str(x.get("d", "")), reverse=True)
        out[ab] = {"n": len(hits),
                   "items": [{"d": h["d"], "a": h["a"], "t": _trim(h["t"], 200)} for h in hits[:3]]}
    return out

