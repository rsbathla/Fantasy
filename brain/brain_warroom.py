def build_today(vault, repo, sm, bi, sem, cs26, dci):
    lamps = [list(run_status(vault)), list(weekly_status(vault)), list(lines_status(repo))]
    threats = sorted(((v["name"], v["avail"]) for v in sm.values()
                      if isinstance(v, dict) and v.get("avail") and v.get("name")),
                     key=lambda r: str(r[1].get("d", "")), reverse=True)[:14]
    threat_rows = [{"n": n, "k": rslug(n), "term": e.get("term", "?"), "x": e.get("n", 1),
                    "d": str(e.get("d", "")),
                    "sev": "red" if any(w in str(e.get("term", "")) for w in ("acl", "achilles", "fractur", "surger")) else "amber"}
                   for n, e in threats]
    tws = todays_tweets(vault)
    lab = Counter(sem.get(t["url"], {}).get("s", "unlabeled") for t in tws if t["url"])
    ment = Counter(m for t in tws for m in t["mentions"]
                   if " " in m and m.split()[-1] not in _TEAM_LAST_WORDS)
    cutoff = (date.today() - timedelta(days=10)).strftime("%Y-%m-%d")
    theses = []
    for name, e in (bi.get("players") or {}).items():
        for f in e.get("fwd", []):
            if str(f.get("d", "")) >= cutoff and "Signal" in str(f.get("s", "")):
                theses.append([f.get("d", ""), name, rslug(name), _trim(f.get("t"), 220)])
    theses.sort(reverse=True)
    srcs = sorted(glob.glob(os.path.join(vault, "Sources", "*.md")), key=os.path.getmtime, reverse=True)[:5]
    rooms = [("Players", "player dossiers"), ("Concepts", "team playbooks"), ("Teams", "team entities"),
             ("Coaches", "coaches"), ("Sources", "articles & transcripts"), ("Tweets", "raw tweets")]
    counts = (bi.get("_meta") or {}).get("counts", {})
    return {
        "lamps": lamps, "threats": threat_rows, "n_tw": len(tws),
        "radar": dict(lab), "most": [[m, rslug(m), c] for m, c in ment.most_common(8)],
        "theses": theses[:6],
        "sources": [os.path.splitext(os.path.basename(s))[0] for s in srcs],
        "rooms": [[d, len(glob.glob(os.path.join(vault, d, "**", "*.md"), recursive=True)), b] for d, b in rooms],
        "counts": counts,
        "n_sig_players": sum(1 for e in (bi.get("players") or {}).values() if e.get("n_sig")),
        "intel_gen": str((bi.get("_meta") or {}).get("generated", ""))[:16],
        # league-wide "where will coverage schemes meaningfully change" table, |man delta| desc
        "scheme_watch": sorted(
            ([ab, e.get("dc_name") or "?", e.get("man_rate_2025"), e.get("man_rate_adj"),
              e.get("conf") or "?", (dci.get(ab) or {}).get("n", 0)]
             for ab, e in cs26.items() if isinstance(e, dict) and e.get("dc_new")),
            key=lambda r: -abs((r[3] or 0) - (r[2] or 0))),
    }

